<script>

    $(document).ready(function() {

        $('#submit-form').on('click', function(e) {
            e.preventDefault();

            const firstName = $('#floating_first_name').val().trim();
            const lastName = $('#floating_last_name').val().trim();
            const email = $('#floating_email').val().trim();
            const password = $('#floating_password').val();
            const confirmPassword = $('#floating_repeat_password').val();

            $.ajax({
                url: '../database/signup.php',
                method: 'POST',
                dataType: 'json',
                data: {
                    'signup': true,
                    first_name: firstName,
                    last_name: lastName,
                    email: email,
                    password: password
                },
                success: function(response) {
                    if (response.success) {
                        Swal.fire({
                            title: response.success,
                            icon: "success",
                            confirmButtonText: "OK",
                            customClass: {
                                popup: "custom-popup", 
                                title: "custom-title",
                                confirmButton: "custom-confirm-btn"
                            }
                        }).then((result) => {
                            if (result.isConfirmed) {
                                window.location.href = "verify.php";
                            }
                        });
                    } else {
                        Swal.fire({
                            title: response.error,
                            icon: "error",
                            confirmButtonText: "OK"
                        })
                    }
                },
                error: function(xhr, status, error) {
                    Swal.fire({
                        title: "Error submitting the form: " + xhr.responseText,
                        icon: "error",
                        confirmButtonText: "OK"
                    })
                }
            });
        });
    });
</script>

